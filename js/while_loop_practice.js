"use strict";

// Counting to 50

var i = 0;

while (i <= 50) {
    console.log(i);
    i++;
}

// Dividing by 5

var g = 250;

while (g > 5) {
	g = g / 5;
	console.log(g);
}

// subtracting 2 each time 

var h = 30;

while (h > 0) {
	h = h - 2;
	console.log(h);
} 


// using a do while

var j = 20

do {
	console.log(j);
	j--;
} while (j >= 20);
