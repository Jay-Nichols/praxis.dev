"use strict";

var dinner = "spaghetti";

switch (dinner) {
	case "lasagna":
		console.log("I had lasagna for dinner");
		break;
	case "pizza":
		console.log("I had pizza for dinner");
		break;
	case "chicken":
		console.log("I had chicken for dinner");
		break;
	case "Whataburger":
		console.log("I had Whataburger for dinner");
		break;
	default:
		console.log("I had " + dinner + " for dinner.");



}